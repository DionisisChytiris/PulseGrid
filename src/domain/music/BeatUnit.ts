/** Notated beat unit for tempo (BPM is measured against this unit). */
export enum BeatUnit {
  WHOLE = 'WHOLE',
  HALF = 'HALF',
  QUARTER = 'QUARTER',
  EIGHTH = 'EIGHTH',
  SIXTEENTH = 'SIXTEENTH',
  THIRTY_SECOND = 'THIRTY_SECOND',

  DOTTED_HALF = 'DOTTED_HALF',
  DOTTED_QUARTER = 'DOTTED_QUARTER',
  DOTTED_EIGHTH = 'DOTTED_EIGHTH',
}
